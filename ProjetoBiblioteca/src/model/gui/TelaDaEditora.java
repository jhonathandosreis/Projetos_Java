    private void jTextFieldPesquisarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldPesquisarKeyPressed

        try {

            String textoDigitado = jTextFieldPesquisar.getText().toLowerCase(); // texto digitado na jtextfield em minuscula

            ArrayList<Editora> resultadoPesquisa = new ArrayList<>(); //lista vazia com o resultado da pesquisa

            for (Editora editora : listaEditoras) {//colaboradores == lista recuperada do arquivo

                if ((editora.getNome().toLowerCase().trim().contains(textoDigitado)) //varre o nome

                        || (("" + editora.getId()).toLowerCase().trim().contains(textoDigitado))) { // varre o id

                    resultadoPesquisa.add(editora); // INCLUI O RESULTADO ENCONTRADO NA LISTA
                }
            }
            imprimirDadosnaGrid(resultadoPesquisa);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_jTextFieldPesquisarKeyPressed
