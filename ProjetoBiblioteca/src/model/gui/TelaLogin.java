    private void criarArquivos() throws Exception {
        try {
            
            for (EnumArquivoTxt nomeArquivo : EnumArquivoTxt.values()) {
                File file = new File(nomeArquivo.getNomeDoArquivo());
                if (!file.exists()) {
                    file.createNewFile();
                }
            }

        } catch (Exception e) {
            throw new Exception("Erro ao criar os arquivos de dados!\n" + e);
        }
    }

