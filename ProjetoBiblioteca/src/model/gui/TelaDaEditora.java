    private void imprimirDadosnaGrid(ArrayList<Editora> lista) {
        DefaultTableModel model = (DefaultTableModel) jTableDadosEditora.getModel();
        model.setNumRows(0);

        for (int pos = 0; pos < lista.size(); pos++) {

            String[] linha = new String[2];
            Editora aux = lista.get(pos);

            linha[0] = "" + aux.getId();
            linha[1] = aux.getNome().toUpperCase();

            model.addRow(linha);

        }
    }

