    private void jTextFieldPesquisarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldPesquisarKeyPressed

        try {

            String textoDigitado = jTextFieldPesquisar.getText().toLowerCase(); // texto digitado na jtextfield em minuscula

            ArrayList<Area> resultadoPesquisa = new ArrayList<>(); //lista vazia com o resultado da pesquisa
            //colaboradores == lista recuperada do arquivo
            for (Area areaDolivro : listaDeArea) {

                if ((("" + areaDolivro.getId()).toLowerCase().trim().contains(textoDigitado))
                        || (("" + areaDolivro.getCdd().getTipo()).toLowerCase().trim().contains(textoDigitado))
                        || (("" + areaDolivro.getCdd()).toLowerCase().trim().contains(textoDigitado))) {

                    resultadoPesquisa.add(areaDolivro); // INCLUI O RESULTADO ENCONTRADO NA LISTA
                }
            }
            imprimirDadosnaGrid(resultadoPesquisa);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_jTextFieldPesquisarKeyPressed

