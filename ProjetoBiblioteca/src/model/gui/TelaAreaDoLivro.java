    public void preencherFormulario() {

        String posicaoId = jTableDadosAreaDoLivro.getValueAt(jTableDadosAreaDoLivro.getSelectedRow(), 0).toString().toUpperCase();
        String posicaoNome = jTableDadosAreaDoLivro.getValueAt(jTableDadosAreaDoLivro.getSelectedRow(), 1).toString().toUpperCase();
        String posicaoCdd = jTableDadosAreaDoLivro.getValueAt(jTableDadosAreaDoLivro.getSelectedRow(), 2).toString().toUpperCase();

        jComboBoxCdd.setSelectedItem(posicaoCdd);
        areaAntiga.setId(Integer.parseInt(posicaoId));
        areaAntiga.setNomeArea(posicaoNome);

    }

    private void imprimirDadosnaGrid(ArrayList<Area> lista) {
        DefaultTableModel model = (DefaultTableModel) jTableDadosAreaDoLivro.getModel();
        model.setNumRows(0);

        for (int pos = 0; pos < lista.size(); pos++) {

            String[] linha = new String[3];
            Area aux = lista.get(pos);

            linha[0] = "" + aux.getId();
            linha[1] = aux.getNomeArea().toUpperCase();
            linha[2] = "" + aux.getCdd().getTipo();

            model.addRow(linha);

        }
    }

    private void limparTela() {

        String t = "";
        jComboBoxCdd.setSelectedItem(t);

    }

